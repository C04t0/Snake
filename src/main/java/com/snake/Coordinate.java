package com.snake;

public class Coordinate {
    public int x = 0;
    public int y =0;

    //Getters en setters

    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
}
